void main() {
// String Data type
  String name = "Bayzid";
  print(name);

// Integer Data type
  int age = 22;
  print(age);

// Double Data type
  double pi = 3.1416;
  print(pi);

// Boolean Data type
  bool condition = true;
  print(condition);

// List 
  List myList = ["one", "two", "three", "four", "five", "six"];
  print(myList[2]);

// Map 
  Map <String, dynamic>myMap = {
    "name": "Bayzid",
    "age": 22,
    "education":"Bsc in CSE",
    "institute":"UODA",
  };
print(myMap["age"]);

// Set
Set mySet = {
  "one", "two", "three", "four", "five", "six"
};
print(mySet.elementAt(2));

}
